export const allTransitions = [...builtInTransitions, ...imaginationTransitions];

// Helper function to check if a transition is an imagination animation
export const isImaginationTransition = (transitionValue) => {
  return transitionValue && transitionValue.startsWith('imagination-');
};

// Get animation file path for imagination transitions
export const getImaginationAnimationPath = (transitionValue) => {
  if (isImaginationTransition(transitionValue)) {
    return `/slideshow-animations/${transitionValue}.gif`;
  }
  return null;
};
